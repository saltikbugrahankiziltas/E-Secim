package com.example.dnemsonuprojesi;

public class CumhurAdayları {
    String cumhurAdayi;
    int image;

    public CumhurAdayları(String cumhurAdayi, int image) {
        this.cumhurAdayi = cumhurAdayi;
        this.image = image;
    }

    public String getCumhurAdayi() {
        return cumhurAdayi;
    }

    public void setCumhurAdayi(String cumhurAdayi) {
        this.cumhurAdayi = cumhurAdayi;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }



}
